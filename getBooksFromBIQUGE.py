def writeBooks():
    try:
        for i in range(len(mm)):
            books = requests.get('http://www.biqugeu.com' + mm[i], cookies=cookies,
                                 headers=header).content.decode('gb2312', 'ignore')
            print('找到网页')
            # 获取网页源代码
            soup = BeautifulSoup(books, 'html.parser')
            # print(soup)
            print('解析网页成功')
            title = soup.find('div', class_='bookname')
            title = soup.find('h1')
            print(title)
            subs = soup.find('div', id='content')
            # 去掉html代码
            reg1 = re.compile("<[^>]*>")
            cont = reg1.sub('', subs.prettify())
            with open(r'2.txt', 'a') as f:
                f.write(str(title))
                f.write(cont)
            print("写入第" + str(i + 1) + "章成功")
            time.sleep(random.randint(20, 35))
    except error.URLError as e:
        print(e.reason)
    print("全部读取完毕")
writeBooks()
